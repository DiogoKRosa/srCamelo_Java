    public String consultarUsuario(String cpf, String senha){
        try{
            ResultSet rs;
            String sql = "SELECT id_cliente FROM Clientes where cpf_cliente = '"+cpf+"' and senha_cliente = '"+senha+"'";
            rs = conn.RetornarResultset(sql);
            rs.beforeFirst();
            if(rs.next()){
                usu.setId(Integer.parseInt(rs.getString(1)));
                return "Cliente";
            }else if(!rs.next()){
                sql="SELECT id_vendedor FROM Vendedor where cpf_vendedor = '"+cpf+"' and senha_vendedor = '"+senha+"'";
                rs = conn.RetornarResultset(sql);
                rs.beforeFirst();
                if(rs.next()){
                    vend.setId(Integer.parseInt(rs.getString(1)));
                    return "Vendedor";
                }else{
                    return "";
                }
            }else{
                return "";
            }
        }catch(SQLException ex){
              JOptionPane.showMessageDialog(null, "Erro ao executar o comando: "+ex);
              return "";
        }
    }
    
    public int consultarCPF(String cpf){
        try{
            ResultSet rs;
            String sql = "SELECT cpf_cliente FROM Clientes where cpf_cliente = '"+cpf+"'";
            rs = conn.RetornarResultset(sql);
            rs.beforeFirst();
            if(rs.next()){
                return 1;
            }else if(!rs.next()){
                sql="SELECT cpf_vendedor FROM Vendedor where cpf_vendedor = '"+cpf+"'";
                rs = conn.RetornarResultset(sql);
                rs.beforeFirst();
                if(!rs.next()){
                    return 0;
                }else{
                    return 1;
                }
            }else{
                return 1;
            }
        }catch(SQLException ex){
              JOptionPane.showMessageDialog(null, "Erro ao executar o comando: "+ex);
              return 1;
        }
    }
    
