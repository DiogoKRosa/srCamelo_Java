    private void btnCadastrarConsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarConsActionPerformed
        String senha = new String(txtSenhacons.getPassword()).trim();
        String ConfirmarSenha = new String(txtSenhacons2.getPassword()).trim();
        int existeCpf;
        
        usu.setNome(txtNomecons.getText());
        usu.setCpf(txtCpfcons.getText());
        usu.setEmail(txtEmailcons.getText());
        usu.setTel(txtTelcons.getText());
        usu.setSenha(senha);
        usu.setEstado(txtUfcons.getText());
        usu.setCidade(txtCidadecons.getText());
        existeCpf = consultarCPF(txtCpfcons.getText());
        if (senha.equals(ConfirmarSenha)){
            if(existeCpf==0){
                usu.cadastrarCliente();
                txtNomecons.setText("");
                txtCpfcons.setText("");
                txtEmailcons.setText("");
                txtTelcons.setText("");
                txtSenhacons.setText("");
                txtSenhacons2.setText("");
                txtUfcons.setText("");
                txtCidadecons.setText("");

                CardLayout cl = (CardLayout) pWrapper.getLayout();
                cl.show(pWrapper, "logar");
            }else{
              JOptionPane.showMessageDialog(null, "CPF já cadastrado");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Senhas são diferentes");
            txtSenhacons2.setText("");
        }
        
    }//GEN-LAST:event_btnCadastrarConsActionPerformed

    private void btnCadastrarCons1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarCons1ActionPerformed
        String senha = new String(txtSenhavend.getPassword()).trim();
        String ConfirmarSenha = new String(txtSenhavend2.getPassword()).trim();
        String tiposPag = "";
        int existeCpf;
        
        vend.setNome(txtNomevend.getText());
        vend.setCpf(txtCpfvend.getText());
        vend.setEmail(txtEmailvend.getText());
        vend.setTel(txtTelvend.getText());
        vend.setSenha(senha);
        vend.setEstado(txtUfvend.getText());
        vend.setCidade(txtCidadevend.getText());
        
        if(btnDinheiro.isSelected()){
            tiposPag = tiposPag.concat("Dinheiro");
        }
        if(btnDebito.isSelected()){
            tiposPag = tiposPag.concat("Debito");
        }
        if(btnCredito.isSelected()){
            tiposPag = tiposPag.concat("Credito");
        }
        if(btnPix.isSelected()){
            tiposPag = tiposPag.concat("Pix");
        }
        vend.setFormaPag(tiposPag);
        
        existeCpf = consultarCPF(txtCpfvend.getText());
        
        if (senha.equals(ConfirmarSenha)){
            if(existeCpf==0){
                vend.cadastrarVendedor();
              
                txtNomevend.setText("");
                txtCpfvend.setText("");
                txtEmailvend.setText("");
                txtTelvend.setText("");
                txtSenhavend.setText("");
                txtSenhavend2.setText("");
                txtUfvend.setText("");
                txtCidadevend.setText("");
                btnDinheiro.setSelected(false);
                btnDebito.setSelected(false);
                btnCredito.setSelected(false);
                btnPix.setSelected(false);
        
                CardLayout cl = (CardLayout) pWrapper.getLayout();
                cl.show(pWrapper, "logar");
            }else{
              JOptionPane.showMessageDialog(null, "CPF já cadastrado");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Senha Divergentes");
            txtSenhavend2.setText("");
        }
        
        
    }//GEN-LAST:event_btnCadastrarCons1ActionPerformed

    private void btnLogar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogar1ActionPerformed
        String cpf = txtCpf.getText();
        String senha = new String(txtSenha.getPassword()).trim();
        String tipo = consultarUsuario(cpf, senha);
        
        if(tipo.equals("Cliente")){
            frmMapaCliente frm = new frmMapaCliente();
            frm.usu.setarIdCliente(usu.getId());
            frm.setVisible(true);
        }else if(tipo.equals("Vendedor")){
            frmVendedor form = new frmVendedor();
            form.vend.setarIdVendedor(vend.getId());
            int valor = form.vendedorNovo();
            //System.out.println(valor);
            //CardLayout cl = (CardLayout) form.pWrapper.getLayout();
            if(valor==1){
                form.setVisible(true);
            }else{
                frmMapaVendedor frm = new frmMapaVendedor();
                frm.vend.setarIdVendedor(vend.getId());
                frm.setVisible(true); 
            }
        }else{
            JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos");
        }
        this.dispose();
    }//GEN-LAST:event_btnLogar1ActionPerformed

